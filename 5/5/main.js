// Создать расширяемый калькулятор

function Calculator() {

  this.methods = {
    '+': (a, b) => a + b,
    '-': (a, b) => a + b,
  }

  this.calculate = function(str) {
    let splited = str.split(' ')
    let a = +splited[0]
    let sign = splited[1]
    let b = +splited[2]

    if (!this.methods[sign] || isNaN(a) || isNaN(b)) {
      return NaN;
    }
    return this.methods[sign](a, b);
  }

  this.addMethod = function(name, func) {
    this.methods[name] = func
  }
}

